numero_dia = int(input("Digite um numero de 1 a 7:"))
if numero_dia == 1:
    print("Domingo")

elif numero_dia == 2:
    print("Segunda Feira")
elif numero_dia == 3:
    print("Terça Feira")
elif numero_dia == 4:
    print("Quarta Feira")
elif numero_dia == 5:
    print("Quinta Feira")
elif numero_dia == 6:
    print("Sexta Feira")
elif numero_dia == 7:
    print("Sabádo")
else:
    print("Digite um numero valido")